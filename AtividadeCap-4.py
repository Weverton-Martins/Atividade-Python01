# 4.1-
import math

x=float(input('Digite um valor para um angulo: '))
x=math.radians(x)

seno=math.sin(x)
cosseno=math.cos(x)
tangente=math.tan(x)

print('Seno: ' ,seno)
print('Seno: ' ,cosseno)
print('Seno: ' ,tangente)

